class AudioManager {
  constructor(library) {
    this.library = library;
    this.music = null;
    this.currentMusicId = null;
    this.fadeTimer = null;
    this.crossfadeTimer = null;
    this.toastTimer = null;
    this.pendingMusicId = null;
    this.unlocked = false;
    this.musicStoppedByUser = false;
    this.victorySfxFlip = false;
    this.wrestlerQueue = [];
    this.storageKey = "catchCardsAudio";
    this.settings = this.loadSettings();
  }

  loadSettings() {
    const defaults = { musicVolume: 0.7, sfxVolume: 0.85, muted: false };
    try {
      return { ...defaults, ...JSON.parse(localStorage.getItem(this.storageKey) || "{}") };
    } catch {
      return defaults;
    }
  }

  saveSettings() {
    try {
      localStorage.setItem(this.storageKey, JSON.stringify(this.settings));
    } catch {
      // Audio preferences are optional.
    }
  }

  createAudio(src, loop = false) {
    if (!src) return null;
    const audio = new Audio(src);
    audio.preload = "none";
    audio.loop = loop;
    audio.addEventListener("error", () => {}, { once: true });
    return audio;
  }

  effectiveVolume(type) {
    if (this.settings.muted) return 0;
    return type === "music" ? this.settings.musicVolume : this.settings.sfxVolume;
  }

  applyMusicVolume() {
    if (this.music) this.music.volume = this.effectiveVolume("music");
  }

  wrestlerMusicIds() {
    return Object.entries(this.library.music)
      .filter(([, item]) => item.wrestler)
      .map(([id]) => id);
  }

  shuffleList(list) {
    const copy = [...list];
    for (let i = copy.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [copy[i], copy[j]] = [copy[j], copy[i]];
    }
    return copy;
  }

  refillWrestlerQueue(excludeId = null) {
    const ids = this.wrestlerMusicIds();
    this.wrestlerQueue = this.shuffleList(ids);
    if (excludeId && this.wrestlerQueue.length > 1 && this.wrestlerQueue[0] === excludeId) {
      this.wrestlerQueue.push(this.wrestlerQueue.shift());
    }
  }

  firstWrestlerMusicId() {
    if (!this.wrestlerQueue.length) this.refillWrestlerQueue();
    return this.wrestlerQueue.shift() || null;
  }

  nextWrestlerMusicId(currentId = this.currentMusicId) {
    if (!this.wrestlerQueue.length) this.refillWrestlerQueue(currentId);
    return this.wrestlerQueue.shift() || null;
  }

  playNextWrestlerMusic(duration = 1600, manual = false) {
    if (this.musicStoppedByUser && !manual) return null;
    if (manual) this.musicStoppedByUser = false;
    const nextId = this.nextWrestlerMusicId();
    if (!nextId) return null;
    this.fadeMusic(nextId, duration, manual);
    return nextId;
  }

  playWrestlerRadio(manual = false) {
    const firstId = this.currentMusicId || this.pendingMusicId || this.firstWrestlerMusicId();
    if (firstId) this.playMusic(firstId, manual);
  }

  attachMusicEvents(audio, id, item) {
    if (!audio) return;
    audio.dataset.crossfadeArmed = "0";
    if (item.wrestler) {
      audio.addEventListener("ended", () => {
        if (this.music === audio) this.playNextWrestlerMusic(1200);
      }, { once: true });
      audio.addEventListener("timeupdate", () => {
        if (this.music !== audio) return;
        if (audio.dataset.crossfadeArmed === "1") return;
        if (!Number.isFinite(audio.duration) || audio.duration <= 4) return;
        if (audio.duration - audio.currentTime <= 2) {
          audio.dataset.crossfadeArmed = "1";
          this.playNextWrestlerMusic(1800);
        }
      });
    }
  }

  startMusic(id, fadeInDuration = 0) {
    const item = this.library.music[id];
    if (!item) return;

    this.music = this.createAudio(item.src, !item.wrestler);
    this.currentMusicId = id;
    if (this.music) this.music.volume = fadeInDuration ? 0 : this.effectiveVolume("music");
    this.updateNowPlaying();
    this.showMusicToast(item);

    if (!this.music) return;
    this.attachMusicEvents(this.music, id, item);
    this.music.play()
      .then(() => {
        this.unlocked = true;
        this.pendingMusicId = null;
        if (fadeInDuration) this.fadeAudioIn(this.music, fadeInDuration);
      })
      .catch(() => {
        this.pendingMusicId = id;
      });
  }

  playMusic(id, manual = false) {
    const item = this.library.music[id];
    if (!item) return;
    if (this.musicStoppedByUser && !manual) return;
    if (manual) this.musicStoppedByUser = false;
    if (this.currentMusicId === id && this.music && !this.music.paused) return;

    if (this.music && !this.music.paused) {
      this.fadeMusic(id, 1400, manual);
      return;
    }
    this.stopMusic(false);
    this.startMusic(id, 500);
  }

  unlockAndPlay() {
    if (this.musicStoppedByUser) return;
    if (this.music && !this.music.paused) {
      this.unlocked = true;
      return;
    }
    this.playMusic(this.pendingMusicId || this.currentMusicId || this.firstWrestlerMusicId());
  }

  stopMusic(manual = true) {
    if (manual) this.musicStoppedByUser = true;
    clearInterval(this.fadeTimer);
    clearInterval(this.crossfadeTimer);
    this.fadeTimer = null;
    this.crossfadeTimer = null;
    this.pendingMusicId = null;
    if (this.music) {
      this.music.pause();
      this.music.currentTime = 0;
    }
    this.music = null;
    this.currentMusicId = null;
    this.updateNowPlaying();
  }

  fadeMusic(id, duration = 800, manual = false) {
    const next = this.library.music[id];
    if (!next) return;
    if (this.musicStoppedByUser && !manual) return;
    if (manual) this.musicStoppedByUser = false;
    if (!this.music || this.music.paused) {
      this.stopMusic(false);
      this.startMusic(id, Math.min(700, duration));
      return;
    }
    if (this.currentMusicId === id) return;

    clearInterval(this.fadeTimer);
    clearInterval(this.crossfadeTimer);
    const oldMusic = this.music;
    const newMusic = this.createAudio(next.src, !next.wrestler);
    if (!newMusic) return;
    newMusic.volume = 0;
    this.attachMusicEvents(newMusic, id, next);

    const oldStartVolume = oldMusic.volume;

    newMusic.play()
      .then(() => {
        this.music = newMusic;
        this.currentMusicId = id;
        this.unlocked = true;
        this.pendingMusicId = null;
        this.updateNowPlaying();
        this.showMusicToast(next);
        const startedAt = performance.now();
        this.crossfadeTimer = setInterval(() => {
          const elapsed = performance.now() - startedAt;
          const progress = Math.min(1, elapsed / Math.max(1, duration));
          const targetVolume = this.effectiveVolume("music");
          oldMusic.volume = oldStartVolume * (1 - progress);
          newMusic.volume = targetVolume * progress;
          if (progress >= 1) {
            clearInterval(this.crossfadeTimer);
            this.crossfadeTimer = null;
            oldMusic.pause();
            oldMusic.currentTime = 0;
            newMusic.volume = this.effectiveVolume("music");
          }
        }, 40);
      })
      .catch(() => {
        this.pendingMusicId = id;
      });
  }

  fadeAudioIn(audio, duration = 500) {
    const startedAt = performance.now();
    const targetVolume = this.effectiveVolume("music");
    const timer = setInterval(() => {
      if (this.music !== audio) {
        clearInterval(timer);
        return;
      }
      const progress = Math.min(1, (performance.now() - startedAt) / Math.max(1, duration));
      audio.volume = targetVolume * progress;
      if (progress >= 1) clearInterval(timer);
    }, 40);
  }

  playSfx(id) {
    if (id === "victoire_duel" && this.library.sfx.victoire_duel_alt) {
      this.victorySfxFlip = !this.victorySfxFlip;
      id = this.victorySfxFlip ? "victoire_duel" : "victoire_duel_alt";
    }
    const item = this.library.sfx[id];
    if (!item) return;
    const sound = this.createAudio(item.src, false);
    if (!sound) return;
    sound.volume = this.effectiveVolume("sfx");
    if (item.maxDuration) {
      sound.addEventListener("play", () => setTimeout(() => {
        sound.pause();
        sound.currentTime = 0;
      }, item.maxDuration), { once: true });
    }
    sound.play().catch(() => {});
  }

  setMusicVolume(value) {
    this.settings.musicVolume = Math.max(0, Math.min(1, Number(value)));
    this.applyMusicVolume();
    this.saveSettings();
  }

  setSfxVolume(value) {
    this.settings.sfxVolume = Math.max(0, Math.min(1, Number(value)));
    this.saveSettings();
  }

  mute() {
    this.settings.muted = true;
    this.applyMusicVolume();
    this.saveSettings();
    this.updateNowPlaying();
  }

  unmute() {
    this.settings.muted = false;
    this.applyMusicVolume();
    this.saveSettings();
    this.updateNowPlaying();
  }

  toggleMute() {
    this.settings.muted ? this.unmute() : this.mute();
  }

  updateNowPlaying() {
    const label = this.currentMusicId ? this.library.music[this.currentMusicId]?.label : "aucun";
    document.querySelectorAll(".current-music-label").forEach(current => {
      current.textContent = this.settings.muted ? `${label} (muet)` : label;
    });
    document.querySelectorAll(".mute-button-control").forEach(button => {
      button.textContent = this.settings.muted ? "Remettre le son" : "Couper le son";
    });
  }

  showMusicToast(item) {
    const toast = document.getElementById("musicToast");
    const name = document.getElementById("musicToastName");
    if (!toast || !name || !item?.wrestler) return;

    clearTimeout(this.toastTimer);
    name.textContent = item.label || item.wrestler;
    toast.classList.add("active");
    this.toastTimer = setTimeout(() => {
      toast.classList.remove("active");
    }, 2000);
  }
}

const audioManager = new AudioManager(AUDIO_LIBRARY);
window.AUDIO_LIBRARY = AUDIO_LIBRARY;
window.audioManager = audioManager;

function initAudioOptions() {
  const wrestlerTracks = Object.entries(AUDIO_LIBRARY.music)
    .filter(([, item]) => item.wrestler)
    .sort(([, a], [, b]) => a.label.localeCompare(b.label, "fr", { sensitivity: "base" }));

  document.querySelectorAll(".music-select-control").forEach(select => {
    select.innerHTML = wrestlerTracks
      .map(([id, item]) => `<option value="${id}">${item.label}</option>`)
      .join("");
    select.addEventListener("change", () => {
      syncMusicSelects(select.value);
      if (audioManager.currentMusicId) audioManager.fadeMusic(select.value, 500);
    });
  });

  document.querySelectorAll(".music-volume-control").forEach(input => {
    input.value = Math.round(audioManager.settings.musicVolume * 100);
    input.addEventListener("input", () => {
      syncControlValues(".music-volume-control", input.value);
      audioManager.setMusicVolume(input.value / 100);
    });
  });

  document.querySelectorAll(".sfx-volume-control").forEach(input => {
    input.value = Math.round(audioManager.settings.sfxVolume * 100);
    input.addEventListener("input", () => {
      syncControlValues(".sfx-volume-control", input.value);
      audioManager.setSfxVolume(input.value / 100);
    });
  });

  document.querySelectorAll(".music-play-control").forEach(button => {
    button.addEventListener("click", () => {
      const selected = document.querySelector(".music-select-control")?.value;
      audioManager.playMusic(selected || audioManager.firstWrestlerMusicId(), true);
    });
  });

  document.querySelectorAll(".music-stop-control").forEach(button => {
    button.addEventListener("click", () => audioManager.stopMusic());
  });

  document.querySelectorAll(".mute-button-control").forEach(button => {
    button.addEventListener("click", () => audioManager.toggleMute());
  });

  document.addEventListener("click", (event) => {
    if (event.target.closest("button,.choice")) audioManager.playSfx("clic");
    if (event.target.closest(".music-stop-control")) return;
    audioManager.unlockAndPlay();
  });

  ["pointerdown", "keydown", "touchstart"].forEach(eventName => {
    document.addEventListener(eventName, () => audioManager.unlockAndPlay(), { once: true });
  });

  audioManager.updateNowPlaying();
}

function syncControlValues(selector, value) {
  document.querySelectorAll(selector).forEach(control => {
    control.value = value;
  });
}

function syncMusicSelects(value) {
  document.querySelectorAll(".music-select-control").forEach(select => {
    select.value = value;
  });
}

function playNextTheme() {
  const nextId = audioManager.playNextWrestlerMusic(500, true);
  if (nextId) syncMusicSelects(nextId);
}

document.addEventListener("DOMContentLoaded", () => {
  initAudioOptions();
  audioManager.playWrestlerRadio();
});
